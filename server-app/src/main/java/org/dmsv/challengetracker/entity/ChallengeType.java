package org.dmsv.challengetracker.entity;

public class ChallengeType {

    private Integer id;
    private String type;

    public ChallengeType() {

    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
